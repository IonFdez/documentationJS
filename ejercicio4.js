/**
 * Multiplies two numbers and returns the result.
 *
 * @param {number} a - The first number to multiply.
 * @param {number} b - The second number to multiply.
 * @returns {number} The product of the two numbers.
 *
 * @example
 * // Returns 20
 * multiply(4, 5);
 */
function multiply(a, b) {
    return a * b;
}